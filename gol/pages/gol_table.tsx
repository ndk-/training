let updateCell = (y: number, x: number, matrix: boolean[][]): boolean => {
    let countNeightbors = 0;
    for (let yy = -1; yy <= 1; yy++) {
        for (let xx = -1; xx <= 1; xx++) {
            if (matrix[y + yy][x + xx])
                if (!(yy == 0 && xx == 0))
                    countNeightbors++;
        }
    }
    if (countNeightbors == 3 && !matrix[y][x]) return true;
    if ((countNeightbors == 2 || countNeightbors == 3) && matrix[y][x]) return true;
    return false;
};

let refreshMatrix = (matrix: any[][]): any[][] => {
    let newMatrix: boolean[][] = [];
    newMatrix[0] = matrix[0];
    for (let y = 1; y < matrix.length - 1; y++) {
        newMatrix[y] = [matrix[y][0]];
        for (let x = 1; x < matrix[y].length - 1; x++) {
            newMatrix[y][x] = updateCell(y, x, matrix);
        }
        newMatrix[y][matrix[y].length - 1] = matrix[y][matrix[y].length - 1];
    }
    newMatrix[matrix.length - 1] = matrix[matrix.length - 1];
    return newMatrix;
}



export default function buildTable() {
    const [matrix, setMatrix] = useState(initMatrix(50, 0.07));
    useEffect(() => {
        let interval = setInterval(() => setMatrix((matrix) => refreshMatrix(matrix)), 50);
        return () => clearInterval(interval);
    }, []);

    return (
        <table>
            <tbody>
                {
                    matrix.map((row, idx) =>
                        <tr key={idx}>
                            {
                                row.map((cell, idx) =>
                                    <td key={idx} className={`${cell}`}>&nbsp;</td>
                                )
                            }
                        </tr>
                    )
                }
            </tbody>
        </table>
    )
}